    @Override
    public MCRContent transform(MCRContent bibtext, MCRParameterCollector parameter) {
        MCRContent content = super.transform(bibtext, parameter);

        if (!(content instanceof MCRStringContent)) {
            return content;
        }

        long start = System.currentTimeMillis();

        String s = ((MCRStringContent) content).asString();
        for (String k : ENTITY_MAP.keySet()) {
            s = s.replaceAll(k, ENTITY_MAP.get(k));
        }

        LOGGER.debug("Replacing html entities took {} ms", System.currentTimeMillis() - start);
        return new MCRStringContent(s);
    }
